#!/usr/bin/env node

const consoleApiUrl = stripTrailingSlash(
  process.env.LOGARYS_CONSOLE_API_URL ?? process.env.PUBLIC_LOGARYS_API_URL ?? "http://localhost:3002"
);
const ingestorApiUrl = stripTrailingSlash(
  process.env.LOGARYS_INGESTOR_API_URL ?? "http://localhost:3000"
);

const pipelineName = process.env.LOGARYS_TEST_PIPELINE_NAME ?? "logarys-test-pipeline";
const pipelineId = process.env.LOGARYS_TEST_PIPELINE_ID ?? pipelineName;
const skipPipelineApi = process.env.LOGARYS_SKIP_PIPELINE_API === "1" || process.env.LOGARYS_SKIP_PIPELINE_API === "true";
const logCount = Number.parseInt(process.env.LOGARYS_TEST_LOG_COUNT ?? "100", 10);
const levels = ["debug", "info", "warning", "error", "critical"];
const maxWaitMs = Number.parseInt(process.env.LOGARYS_TEST_WAIT_MS ?? "60000", 10);
const retryDelayMs = Number.parseInt(process.env.LOGARYS_TEST_RETRY_DELAY_MS ?? "1000", 10);

function stripTrailingSlash(value) {
  return value.replace(/\/$/, "");
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function requestJson(url, options = {}) {
  const response = await fetch(url, {
    ...options,
    headers: {
      "Content-Type": "application/json",
      Accept: "application/json",
      ...(options.headers ?? {})
    }
  });

  const text = await response.text();
  let data = null;

  if (text.length > 0) {
    try {
      data = JSON.parse(text);
    } catch {
      data = text;
    }
  }

  return {
    ok: response.ok,
    status: response.status,
    text,
    data
  };
}

async function waitForHttpService(name, baseUrl) {
  const start = Date.now();
  let lastError = "not checked yet";

  while (Date.now() - start < maxWaitMs) {
    try {
      const response = await fetch(baseUrl, { method: "GET" });
      console.log(`${name} is reachable on ${baseUrl} with HTTP ${response.status}`);
      return;
    } catch (error) {
      lastError = error instanceof Error ? error.message : String(error);
      process.stdout.write(".");
      await sleep(retryDelayMs);
    }
  }

  console.log("");
  throw new Error(
    `${name} is not reachable on ${baseUrl} after ${maxWaitMs}ms. Last error: ${lastError}\n` +
      `Check containers with: docker compose ps\n` +
      `Check logs with: docker compose logs --tail=100 console-manager ingestor storage-manager mongodb nats`
  );
}

async function firstSuccessfulRequest(baseUrl, candidates, body, method = "POST") {
  const errors = [];

  for (const path of candidates) {
    const url = `${baseUrl}${path}`;
    try {
      const response = await requestJson(url, {
        method,
        body: body === null ? undefined : JSON.stringify(body)
      });

      if (response.ok) {
        return { url, response };
      }

      errors.push(`${url} -> HTTP ${response.status}: ${response.text}`);
    } catch (error) {
      errors.push(`${url} -> ${error instanceof Error ? error.message : String(error)}`);
    }
  }

  throw new Error(errors.join("\n"));
}

async function createPipeline() {
  if (skipPipelineApi) {
    console.log("Skipping pipeline API creation because LOGARYS_SKIP_PIPELINE_API is enabled.");
    return {
      endpoint: "mongodb fallback",
      pipeline: { id: pipelineId, name: pipelineName, enabled: true, inputType: "http" },
      pipelineId
    };
  }

  const pipeline = {
    id: pipelineId,
    name: pipelineName,
    enabled: true,
    inputType: "http",
    description: "Test pipeline generated by scripts/load-test-data.mjs",
    config: {
      source: "load-test-data",
      format: "json"
    }
  };

  const candidates = [
    "/config-management/pipelines",
    "/api/config-management/pipelines",
    "/config-management/pipeline",
    "/api/config-management/pipeline",
    "/configuration/pipelines",
    "/api/configuration/pipelines",
    "/pipeline-configs",
    "/api/pipeline-configs",
    "/pipeline-config",
    "/api/pipeline-config",
    "/config/pipelines",
    "/api/config/pipelines",
    "/config/pipeline",
    "/api/config/pipeline",
    "/pipelines",
    "/api/pipelines",
    "/pipeline",
    "/api/pipeline"
  ];

  const { url, response } = await firstSuccessfulRequest(consoleApiUrl, candidates, pipeline, "POST");
  const created = response.data ?? {};

  return {
    endpoint: url,
    pipeline: created,
    pipelineId: created.id ?? created._id ?? created.name ?? pipelineId
  };
}

function createLog(index, currentPipelineId) {
  const level = levels[index % levels.length];
  const now = Date.now();

  return {
    pipelineId: currentPipelineId,
    pipelineName,
    level,
    message: `Synthetic ${level} log #${String(index + 1).padStart(3, "0")}`,
    timestamp: new Date(now - (logCount - index) * 1000).toISOString(),
    source: "load-test-data",
    context: {
      index: index + 1,
      userAgent: "logarys-test-loader/1.0",
      requestId: `test-${String(index + 1).padStart(3, "0")}`,
      service: index % 2 === 0 ? "checkout" : "catalog",
      durationMs: 10 + index * 3
    }
  };
}

function logIngestCandidates() {
  return [
    "/logs",
    "/api/logs",
    "/ingest",
    "/api/ingest",
    `/ingest/${encodeURIComponent(pipelineName)}`,
    `/api/ingest/${encodeURIComponent(pipelineName)}`,
    `/ingest/${encodeURIComponent(pipelineId)}`,
    `/api/ingest/${encodeURIComponent(pipelineId)}`,
    "/input/http",
    "/api/input/http",
    `/input/${encodeURIComponent(pipelineName)}`,
    `/api/input/${encodeURIComponent(pipelineName)}`,
    `/input/${encodeURIComponent(pipelineId)}`,
    `/api/input/${encodeURIComponent(pipelineId)}`,
    `/${encodeURIComponent(pipelineName)}`,
    `/api/${encodeURIComponent(pipelineName)}`,
    `/${encodeURIComponent(pipelineId)}`,
    `/api/${encodeURIComponent(pipelineId)}`
  ];
}

async function pushOneLog(log) {
  return firstSuccessfulRequest(ingestorApiUrl, logIngestCandidates(), log, "POST");
}

async function main() {
  if (!Number.isInteger(logCount) || logCount <= 0) {
    throw new Error("LOGARYS_TEST_LOG_COUNT must be a positive integer");
  }

  if (!skipPipelineApi) {
    console.log(`Waiting for console manager on ${consoleApiUrl}`);
    await waitForHttpService("Console manager", consoleApiUrl);
  }

  console.log(`Waiting for ingestor on ${ingestorApiUrl}`);
  await waitForHttpService("Ingestor", ingestorApiUrl);

  console.log(`Creating test pipeline on ${consoleApiUrl}...`);
  const created = await createPipeline();
  console.log(`Pipeline created through ${created.endpoint}`);
  console.log(`Pipeline id: ${created.pipelineId}`);

  console.log(`Sending ${logCount} logs to ${ingestorApiUrl}...`);

  let endpoint = null;
  for (let index = 0; index < logCount; index += 1) {
    const log = createLog(index, created.pipelineId);
    const result = await pushOneLog(log);
    endpoint = endpoint ?? result.url;

    if ((index + 1) % 10 === 0 || index + 1 === logCount) {
      console.log(`Sent ${index + 1}/${logCount}`);
    }
  }

  console.log(`Logs sent through ${endpoint}`);
  console.log("Done.");
}

main().catch((error) => {
  console.error("Unable to load test data.");
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
